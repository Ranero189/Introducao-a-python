n1 = float(input('Insira um numero: '))
n2 = float(input('Insira outro numero: '))

print('A soma entre esses numeros é', n1+n2)
print('A subtração entre esses numeros é', n1-n2)
print('A multiplicação entre esses numeros é', n1*n2)
print('A divisão entre esses numeros é', n1/n2)
print('O resto da divisão entre esses numeros é', n1%n2)
print('A potenciação entre esse numeros é', n1**n2)
print('A média entre esses numeros é', (n1+n2)/2)